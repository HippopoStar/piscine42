#include <stdio.h>

int		check_arg(char **arg)
{
	int		i;
	int		j;

	i = 1;
	j = 0;
	while (arg[i])
	{
		while (arg[i][j])
		{
			if (!(arg[i][j] == '.' || (arg[i][j] >= 49 && arg[i][j] <= 57)))
				return (0);
			j++;
		}
		if (j != 9)
			return (0);
		j = 0;
		i++;
	}
	if (i != 10)
		return (0);
	return (1);
}

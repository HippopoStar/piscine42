char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (src[j] != '\0' && j < n - 1)
	{
		dest[i + j] = src[j];
		j++;
	}
	dest[i + j] = '\0';
}
